    def renderAsValidationDependency(self, templatePath: str):

        currentReferencePath = self.iif( 
                self.parentTemplateReferencePath_ is not None,
                self.parentTemplateReferencePath_,
                self.templateReferencePath_)

        subContext = RendererContext(
            customControler= self.controler, 
            templateReferencePath= currentReferencePath,
            parentTemplateReferencePath=os.path.join(
                self.templateReferencePath_, 
                os.path.dirname(templatePath))
        )

        templatePathSize = len(templatePath)
        currentOutput: str = os.path.join(currentReferencePath,
            templatePath[0:templatePathSize - 3])

        subContext.renderTemplate(
            templatePath= templatePath,
            data = self.currentRenderingContext.currentInput,
            output= currentOutput)

        return ''
        
    def renderTemplate(
        self, 
        templatePath: str, 
        data: object, 
        output: codecs.StreamReaderWriter = sys.stdout,
        dryRunSwitch: bool = False) -> str:
            
            self.currentRenderingContext = RendererContextRenderingInstance(
                currentInput=data, currentTemplate=templatePath,
                parentContext=self, currentOutput=output)

            # TODO implement infinite loops detection

            self.currentData = data
            
            self.templateBaseDir_ =  os.path.dirname(templatePath)

            self.logger_.info(f'template reference path is: {self.templateReferencePath_}')
            self.logger_.info(f'rendering {templatePath} to {output} ...')
            
            if isinstance(output, str) and \
                os.path.exists(output) and \
                os.path.isfile(output):
                    self.logger_.info(f'{output} exists and is a file, overwriting ...')

            try :
                template = self.jinjaTemplateEnv_.get_template(templatePath)
                if ( not dryRunSwitch ):
                    template.stream(data, context= self, os=os).dump(output)
            except Exception as e:
                typeName: str = type(e).__name__
                errorMessage: str = None
                if typeName == 'TemplateSyntaxError':
                    errorMessage = f'ko : Exception {typeName} in {e.name} => {e.message} at line {e.lineno}'
                else:
                    errorMessage = f'ko : Exception {typeName} => {e}'
                self.logger_.error(errorMessage)
                raise e
            else :
                self.logger_.info(f'{templatePath} rendered')
            finally :
                if type(output) == 'codecs.StreamReaderWriter' :
                    output.close()

