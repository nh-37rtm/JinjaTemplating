class RendererContextRenderingInstance():
    def __init__(
            self, 
            currentInput, parentContext, currentOutput, 
            currentTemplate):

        self.currentInput: str = currentInput
        self.parentContext: 'RendererContext' = parentContext
        self.currentOutput = currentOutput
        self.currentTemplate = currentTemplate

class RendererContext:

    logger_ = logging.getLogger("root")
    templateBaseDir_ : str = None
    templateReferencePath_ : str = None
    parentTemplateReferencePath_ : str = None
    currentRenderingContext: currentRenderingContext = None
    kube_api_: client.CoreV1Api = None

    # default constructor
    def __init__(self, customControler, templateReferencePath = os.getcwd(), parentTemplateReferencePath= None):
        self.templateReferencePath_ = templateReferencePath
        self.jinjaTemplateLoader_ = FileSystemLoader(templateReferencePath)
        self.jinjaTemplateEnv_ = Environment(loader=self.jinjaTemplateLoader_)
        self.controler = customControler
        self.logger_.info("initializing kube client ...")
        config.load_kube_config()
        self.kube_api_ = client.CoreV1Api(client.ApiClient())
        self.parentTemplateReferencePath_ = parentTemplateReferencePath


    def getHeader(self, commentCaracter: str = '#'):
        return f'{commentCaracter} do not modify (autogenerated by CI)'

    def getHeaderWithTimestamp(self, commentCaracter: str = '#'):
        return f'{commentCaracter} do not modify (autogenerated by CI) at {self.getDateTime()}'

    def kubeSecretExists(self, name: str, namespace: str) -> bool:
        self.logger_.info(f"looking for secret {name} in namespace {namespace} ...")
        try:
            _=self.kube_api_.read_namespaced_secret(
                namespace=namespace, name=name
            )
            return True
        except client.exceptions.ApiException as exception:
            if exception.status == 404:
                return False

    def linfo(self, text: str):
        self.logger_.info(text)
        return '' # avoid printing None in templates
    def getEnv(self, varName: str):
        return os.environ[varName]
    def getDateTime(self):
        return datetime.now().strftime("%H:%M:%S")

    def generatePassword(self):
        self.logger_.info("generating password...")
        result_str = ''.join(random.choice( ''.join( [string.ascii_letters, '.*-_()[]'])) for i in range(10))
        return result_str

    def urlJoin(self, base, url):
        return urljoin(base=base, url=url, allow_fragments=True)

    def iif(self, condition, value1: str, value2: str) -> str:
        if condition and value1: 
            return value1
        return value2

    def default(self, value1: str, value2: str) -> str:
        if value1: 
            return value1
        return value2

    def getCurrentData():
        return currentData


